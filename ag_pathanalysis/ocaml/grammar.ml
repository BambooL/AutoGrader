let label_parse s =
  if String.sub s 0 4 = "addr"
     then Addr (Big_int_Z.big_int_of_string (String.sub s 5 (String.length s - 5)))
     else if String.sub s 0 5 = "label"
          then Name (String.sub s 6 (String.length s - 6))
          else err "Unparsable label"


let mk_attr lab string =
  match lab with
  | "asm" -> Asm string
  | "address" -> Address(Big_int_Z.big_int_of_string string)
  | "target" -> Target(label_parse string)
  | "set" when string = "liveout" -> Liveout
  | "set" when string = "initro" -> InitRO
  | "set" when string = "synthetic" -> Synthetic
  | "set" when string = "specialblock" -> SpecialBlock
  | "str" | "attr" -> StrAttr string
  | "tid" -> ThreadId(int_of_string string)
  | _ -> err ("Unknown attribute @"^lab)

let mk_attr2 lab string1 string2 =
  match lab with
  | "namedstr" -> NamedStrAttr (string1, string2)
  | _ -> err ("Unknown attribute @"^lab)

let mk_context =
  let memre = Str.regexp "^mem\\[\\([^][]+\\)\\]$" in
  (fun name value taint typ usage ->
    if Str.string_match memre name 0 then
      (* Memory value *)
      let index = Str.matched_group 1 name in
      let index = Big_int_Z.big_int_of_string index in
      {name=name; mem=true; t=typ; index=index; value=value; usage=usage; taint=taint}
    else
      {name=name; mem=false; t=typ; index=big_int_of_int 0; value=value; usage=usage; taint=taint}
  )

let usage_of_string = function
  | "rd" | "RD" -> RD
  | "rw" | "RW" -> RW
  | "wr" | "WR" -> WR
  | s -> err ("Unknown operand usage "^s)

let typ_of_string = 
  let tre = Str.regexp "^u\\([0-9]+\\)$" in
  function
  | "bool" -> reg_1
  | "u8" -> reg_8
  | "u16" -> reg_16
  | "u32" -> reg_32
  | "u64" -> reg_64
  | s when Str.string_match tre s 0 ->
      Reg(int_of_string (Str.matched_group 1 s))
  | s -> err ("Unexpected type '"^s^"'")

let casttype_of_string = function
  | "pad"     -> CAST_UNSIGNED
  | "extend"  -> CAST_SIGNED  
  | "high"    -> CAST_HIGH    
  | "low"     -> CAST_LOW     
  | s -> err("Unexpected cast type '"^s^"'")

